            - 116073.* DL^2
            + 39173.* DL^3
            - 1020.8* DL1^3
            - 13864.* DL1^2
            - 100922.* DL1
            + 343243.* DL*DL1
      );,
      nf == 5,
      P3qgApp1 = (
            P3QG01
            + 312500.* YM*DL
