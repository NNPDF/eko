Part proportional to Zeta[5] and the quartic Casimir,
see Eq. 3.18, 3.19 of 1805.09638.
*)
gqg3z5N =(
  + (d4RA/na)*nf* 640/3*z5* (
       391/2 * 1/(n)
     - 202 * 1/(n+1)
     - 30 * 1/(n+2)
     -  8 * 1/(n-1)
     - 63 * 1/(n)^2
     - 126* 1/(n+1)^2
     + 24 * 1/(n+2)^2
     + 24* (1/(n)-2*1/(n+1)+2*1/(n+2))* S[1,n]
     )
  + (d4RR/na)*nf^2* 1280/3*z5* (
     - 68 * 1/(n)
     + 64 * 1/(n+1)
     +  8 * 1/(n+2)
     + 24 * 1/(n)^2
     + 48 * 1/(n+1)^2
    )
);

(*
Large-N limit including the first 2 leading logs: Log[1-x]^5, Log[1-x]^4
from 0912.0369, Eq 5.24
*)
gqg3Ninf = - (
	+ (22/27 caf^3 nf -14/27 caf^2 cf nf -4/27 caf^2 nf^2) * Lm15[n]
	+ (
		(293/27 -80/9 z2) caf^3 nf + (4477/162 - 8 z2) caf^2 cf nf
		- 13/81 caf cf^2 nf -116/81 caf^2 nf^2 +17/81 caf cf nf^2 -4/81 caf nf^3
	) * Lm14[n]
);


