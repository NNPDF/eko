def test_recursive_harmonic_sum():

    n = np.random.rand()
    iterations = 1
    sx_base = harmonics.sx(n)
    sx_test = harmonics.sx(n + iterations)

    sx_final = []
    for w in range(1, 6):
        sx_final.append(
            harmonics.polygamma.recursive_harmonic_sum(sx_base[w - 1], n, iterations, w)
        )

    np.testing.assert_allclose(sx_final, sx_test)
